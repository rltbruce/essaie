<?php  if ( ! defined('BASEPATH')) exit('No direct script access allowed');

class Plan_comptable_model extends CI_Model {
    protected $table = 'plan_comptable';

    public function add($plan_comptable) {
        $this->db->set($this->_set($plan_comptable))
                            ->insert($this->table);
        if($this->db->affected_rows() === 1) {
            return $this->db->insert_id();
        }else{
            return null;
        }                    
    }
    public function update($id, $plan_comptable) {
        $this->db->set($this->_set($plan_comptable))
                            ->where('id', (int) $id)
                            ->update($this->table);
        if($this->db->affected_rows() === 1)
        {
            return true;
        }else{
            return null;
        }                      
    }
    public function _set($plan_comptable) {
        return array(
            'code'          =>      $plan_comptable['code'],
            'description'   =>      $plan_comptable['description']                       
        );
    }
    public function delete($id) {
        $this->db->where('id', (int) $id)->delete($this->table);
        if($this->db->affected_rows() === 1)
        {
            return true;
        }else{
            return null;
        }  
    }
    public function findAll() {               
        $result =  $this->db->select('*')
                        ->from($this->table)
                        ->order_by('description')
                        ->get()
                        ->result();
        if($result)
        {
            return $result;
        }else{
            return null;
        }                 
    }
    public function findById($id)  {
        $this->db->where("id", $id);
        $q = $this->db->get($this->table);
        if ($q->num_rows() > 0) {
            return $q->row();
        }
    }

}
